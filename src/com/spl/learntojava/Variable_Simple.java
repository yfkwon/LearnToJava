package com.spl.learntojava;

/**
 *
 */
public class Variable_Simple {
    /**
     *
     * @param argv
     */
    public static void main(String [] argv) {
        int a = 0;
        a = 1;

        System.out.println(a+1);

        a = 2;
        System.out.println(a+1);

        String first = "coding";
        System.out.println(first + " " + "everybody");

        String second = "everybody, too";
        System.out.println(first + " " + second);

    }
}
