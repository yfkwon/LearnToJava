package com.spl.learntojava;

public class ConditionSwitch {
    public static void main(String [] args) {

        System.out.println("switch");

        int condition = 1;

        switch(condition) {
            case 1:
                System.out.println("one");
                break;
            case 2:
                System.out.println("two");
                break;
            case 3:
                System.out.println("three");
        }
    }
}
